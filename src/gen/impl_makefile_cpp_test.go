func TestCppMakefileGenerator_Registry(t *testing.T) {
	gen, ok := Get("impl_makefile_cpp")
	if !ok {
		t.Fatal("impl_makefile_cpp generator not found in registry")
	}
	if gen.Name() != "impl_makefile_cpp" {
		t.Errorf("expected name %q, got %q", "impl_makefile_cpp", gen.Name())
	}
}

func TestCppMakefileGenerator_Generate(t *testing.T) {
	ctx := loadTestAPI(t, "minimal.yaml")
	gen := &CppMakefileGenerator{}

	files, err := gen.Generate(ctx)
	if err != nil {
		t.Fatalf("generation failed: %v", err)
	}

	if len(files) != 1 {
		t.Fatalf("expected 1 file, got %d", len(files))
	}
	if files[0].Path != "Makefile" {
		t.Errorf("expected path Makefile, got %q", files[0].Path)
	}
	if !files[0].Scaffold {
		t.Error("Makefile should be scaffold")
	}
}

func TestCppMakefileGenerator_Content(t *testing.T) {
	ctx := loadTestAPI(t, "minimal.yaml")
	gen := &CppMakefileGenerator{}

	files, err := gen.Generate(ctx)
	if err != nil {
		t.Fatalf("generation failed: %v", err)
	}

	content := string(files[0].Content)

	// Header
	if !strings.Contains(content, "Generated by xplatter") {
		t.Error("missing generated file header")
	}
	if !strings.Contains(content, "IMPL_LANG := cpp") {
		t.Error("missing IMPL_LANG")
	}

	// C++ specific
	if !strings.Contains(content, "CXX        ?= c++") {
		t.Error("missing CXX variable")
	}
	if !strings.Contains(content, "-std=c++20") {
		t.Error("missing C++20 standard flag")
	}
	if !strings.Contains(content, "SHIM_SOURCE") {
		t.Error("missing SHIM_SOURCE variable")
	}

	// Local build
	if !strings.Contains(content, "$(CXX) $(CXXFLAGS)") {
		t.Error("missing C++ compilation command")
	}
	if !strings.Contains(content, "main.cpp") {
		t.Error("missing main.cpp in run target")
	}

	// Shared lib
	if !strings.Contains(content, "shared-lib: $(SHARED_LIB)") {
		t.Error("missing shared-lib target")
	}
	if !strings.Contains(content, "-shared -fPIC") {
		t.Error("missing shared library flags")
	}

	// Codegen stamp with -o generated
	if !strings.Contains(content, "$(XPLATTER) generate --impl-lang cpp -o generated") {
		t.Error("missing codegen stamp rule with -o generated")
	}

	// Include path derived from GEN_DIR, not hardcoded
	if !strings.Contains(content, "-I$(GEN_DIR)") {
		t.Error("missing -I$(GEN_DIR) in CXXFLAGS")
	}
	if !strings.Contains(content, "/Fo:$(BUILD_DIR)/") {
		t.Error("missing /Fo: flag to redirect MSVC obj files into build/")
	}

	// SHIM_SOURCE uses $(GEN_DIR) prefix
	if !strings.Contains(content, "SHIM_SOURCE    := $(GEN_DIR)$(API_NAME)_shim.cpp") {
		t.Error("missing $(GEN_DIR) prefix on SHIM_SOURCE")
	}

	// Clean removes generated/
	if !strings.Contains(content, "rm -rf generated") {
		t.Error("clean target should remove generated/")
	}

	// WASM exports
	if !strings.Contains(content, "WASM_EXPORTS") {
		t.Error("missing WASM_EXPORTS")
	}

	// Packaging targets
	if !strings.Contains(content, "package-ios") {
		t.Error("missing package-ios target")
	}
	if !strings.Contains(content, "package-android") {
		t.Error("missing package-android target")
	}
	if !strings.Contains(content, "package-web") {
		t.Error("missing package-web target")
	}
	if !strings.Contains(content, "package-desktop") {
		t.Error("missing package-desktop target")
	}
	if !strings.Contains(content, "packages: $(PACKAGE_TARGETS)") {
		t.Error("missing aggregate packages target")
	}
}

func TestCppMakefileGenerator_IOSRules(t *testing.T) {
	ctx := loadTestAPI(t, "minimal.yaml")
	gen := &CppMakefileGenerator{}

	files, err := gen.Generate(ctx)
	if err != nil {
		t.Fatalf("generation failed: %v", err)
	}

	content := string(files[0].Content)

	if !strings.Contains(content, "define BUILD_IOS_ARCH") {
		t.Error("missing BUILD_IOS_ARCH macro")
	}
	if !strings.Contains(content, "xcrun --sdk $(3) $(CXX)") {
		t.Error("missing xcrun C++ compilation in iOS arch rule")
	}
	if !strings.Contains(content, "ios-arm64,arm64-apple-ios") {
		t.Error("missing ios-arm64 arch evaluation")
	}
}

